----------------------------------------------------
-- Initial Load
----------------------------------------------------

SELECT *
FROM Customer;

----------------------------------------------------
-- Incremental Load
----------------------------------------------------

SELECT *
FROM Orders
WHERE ModifiedDate >
'2025-05-01';

----------------------------------------------------
-- Backfill Load
----------------------------------------------------

SELECT *
FROM Orders
WHERE ModifiedDate >= '2025-01-01'
AND ModifiedDate < '2025-02-01';

----------------------------------------------------
-- Latest Watermark
----------------------------------------------------

SELECT MAX(ModifiedDate)
FROM Orders;